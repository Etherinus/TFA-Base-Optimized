local languages = {
    en = "en",
    us = "en",
    gb = "en",
    ru = "ru",
    fr = "fr",
    be = "fr",
    de = "de",
    ge = "de",
    at = "de",
    ch = "de"
}

local function resolveLanguage(preferred)
    local code = preferred

    if not code then
        local cvar = GetConVar and GetConVar("cl_tfa_language")

        if cvar then
            code = cvar:GetString()
        end
    end

    if code and code ~= "" then
        code = string_lower(code)
        code = languages[code] or code

        if langstrings[code] then
            return code
        end
    end

    local country = system and system.GetCountry and system.GetCountry()

    if country then
        country = string_lower(country)
        country = languages[country] or country
    end

    if country and langstrings[country] then
        return country
    end

    return "en"
end

function TFA.GetLangString(key, preferred)
    local lang = resolveLanguage(preferred)
    local langtbl = langstrings[lang] or langstrings.en
    local res = langtbl[key] or langstrings.en[key]

    return res or key
end
